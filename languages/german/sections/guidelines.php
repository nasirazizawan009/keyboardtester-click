                </details>
                <details class="faq-item">
                    <summary>Was ist der Unterschied zwischen QWERTZ und QWERTY?</summary>
                    <p>QWERTZ ist das deutsche Tastaturlayout, bei dem die Tasten Z und Y gegenueber dem englischen QWERTY-Layout vertauscht sind. Zusaetzlich gibt es die Umlaute Ae, Oe, Ue und das Scharfes S (ss).</p>
                </details>
