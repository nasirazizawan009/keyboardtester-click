  <h4>3.4 인디케이터 라이트</h4>
  <p>숫자 패드 위의 라이트는 다음 상태를 표시합니다:</p>
  <ul>
    <li><strong>Caps Lock</strong>: 대문자 잠금 시 초록색.</li>
    <li><strong>Num Lock</strong>: 숫자 패드 활성화 시 초록색.</li>
    <li><strong>Scroll Lock</strong>: 스크롤 잠금 시 초록색.</li>
  </ul>
  <p>이 라이트는 키보드의 실제 상태를 반영합니다.</p>

  <h4>3.5 고급 활용</h4>
  <p>다양한 용도로 활용 가능:</p>
  <ul>
    <li><strong>접근성 테스트</strong>: 장애가 있는 사용자를 위한 키 입력 확인.</li>
    <li><strong>소프트웨어 개발</strong>: 한국어 입력 버그 디버깅.</li>
