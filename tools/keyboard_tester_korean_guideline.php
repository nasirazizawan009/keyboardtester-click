  <ul>
    <li><strong>입력기 확인</strong>: Windows에서 한국어 2-Set 입력기가 활성화되었는지 확인 ('한/영' 키).</li>
    <li><strong>입력 기록 오류</strong>: 잘못된 문자 표시 시 초기화 후 재테스트.</li>
    <li><strong>브라우저 설정</strong>: 브라우저 언어 설정을 한국어로 확인.</li>
