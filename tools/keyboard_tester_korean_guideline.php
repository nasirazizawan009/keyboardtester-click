  <p>오른쪽 상단 토글 스위치로 밝은 모드와 어두운 모드를 전환할 수 있습니다:</p>
  <ul>
    <li><strong>밝은 모드</strong>: 낮에 선명한 색상으로 테스트.</li>
    <li><strong>어두운 모드</strong>: 밤에 눈의 피로를 줄임.</li>
  </ul>
