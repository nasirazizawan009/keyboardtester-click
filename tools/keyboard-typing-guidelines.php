  A: Shortcuts are counted as regular keystrokes. The test measures your ability to type the displayed text, so use standard typing methods.</p>
  
  <p><strong>Q: How is accuracy calculated?</strong><br>
  A: Accuracy = (Total Characters - Errors) / Total Characters √ó 100%. You must type exactly what's shown.</p>
  
