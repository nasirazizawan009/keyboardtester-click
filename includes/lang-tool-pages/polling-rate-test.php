    var avgEl=document.getElementById('pr-avg-hz');
    var peakEl=document.getElementById('pr-peak-hz');
    var samplesEl=document.getElementById('pr-samples');
    var hzBar=document.getElementById('pr-hz-bar');
    var barWrap=document.getElementById('pr-bar-wrap');
    var ratingEl=document.getElementById('pr-rating');
    var testZone=document.getElementById('pr-test-zone');
    var startBtn=document.getElementById('pr-start');
    var stopBtn=document.getElementById('pr-stop');
    var resetBtn=document.getElementById('pr-reset');

    var active=false,lastTime=null,samples=[],peakHz=0,totalSamples=0;
    var WINDOW=60; // rolling average window

    function getHz(now){
      if(lastTime===null){lastTime=now;return 0;}
      var delta=now-lastTime;
      lastTime=now;
      if(delta<=0)return 0;
      return Math.round(1000/delta);
    }

    function onMouseMove(e){
      if(!active)return;
      var now=performance.now();
      var hz=getHz(now);
      if(hz<=0||hz>10000)return;
      totalSamples++;
      samples.push(hz);
      if(samples.length>WINDOW)samples.shift();
      var avg=Math.round(samples.reduce(function(a,b){return a+b;},0)/samples.length);
      if(hz>peakHz)peakHz=hz;
      liveEl.textContent=hz;
      avgEl.textContent=avg;
      peakEl.textContent=peakHz;
      samplesEl.textContent=totalSamples;
      var pct=Math.min(hz/1000*100,100);
      hzBar.style.width=pct+'%';
      barWrap.setAttribute('aria-valuenow',hz);
      ratingEl.textContent=getRating(avg);
    }

    function getRating(hz){
      if(hz>=4000)return '🚀 Ultra (4000+ Hz)';
      if(hz>=1000)return '🏆 Pro Gaming (1000 Hz)';
      if(hz>=500)return '⚡ Gaming (500 Hz)';
      if(hz>=125)return '✅ Standard (125 Hz)';
      return '📡 Measuring...';
    }

    startBtn.addEventListener('click',function(){
      active=true;
      lastTime=null;
      testZone.classList.add('active');
      startBtn.disabled=true;
      stopBtn.disabled=false;
      testZone.addEventListener('mousemove',onMouseMove);
    });

    stopBtn.addEventListener('click',function(){
      active=false;
      testZone.classList.remove('active');
      startBtn.disabled=false;
      stopBtn.disabled=true;
      testZone.removeEventListener('mousemove',onMouseMove);
    });

    resetBtn.addEventListener('click',function(){
      active=false;
      lastTime=null;
      samples=[];
      peakHz=0;
      totalSamples=0;
      liveEl.textContent='0';
      avgEl.textContent='0';
      peakEl.textContent='0';
      samplesEl.textContent='0';
      hzBar.style.width='0%';
      barWrap.setAttribute('aria-valuenow',0);
      ratingEl.textContent='';
      testZone.classList.remove('active');
      testZone.removeEventListener('mousemove',onMouseMove);
      startBtn.disabled=false;
      stopBtn.disabled=true;
    });
  }());
  </script>
</body>
