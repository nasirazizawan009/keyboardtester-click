  <script>
  (function(){
    var box=document.getElementById('rt-box');
    var boxText=document.getElementById('rt-box-text');
    var boxSub=document.getElementById('rt-box-sub');
    var statsWrap=document.getElementById('rt-stats');
    var statLast=document.getElementById('rt-stat-last');
    var statBest=document.getElementById('rt-stat-best');
    var statAvg=document.getElementById('rt-stat-avg');
    var statCount=document.getElementById('rt-stat-count');
    var histList=document.getElementById('rt-history-list');

    var S_WAITING='waiting',S_READY='get-ready',S_TOO_EARLY='too-early',S_GO='go',S_RESULT='result';
    var state=S_WAITING,timer=null,goTime=null,results=[];

    var txt={
      pressHint:<?php echo json_encode($d['press_hint']); ?>,
      waitMsg:<?php echo json_encode($d['wait_msg']); ?>,
      waitSub:<?php echo json_encode($d['wait_sub']); ?>,
      tooEarly:<?php echo json_encode($d['too_early']); ?>,
      tooEarlySub:<?php echo json_encode($d['too_early_sub']); ?>,
      goMsg:<?php echo json_encode($d['go_msg']); ?>,
      resultPrefix:<?php echo json_encode($d['result_prefix']); ?>,
      resultSub:<?php echo json_encode($d['result_sub']); ?>
    };

    function setState(s){
      state=s;
      box.className='';
      box.classList.add('state-'+s);
    }
    function setContent(main,sub){
      boxText.textContent=main;
      boxSub.textContent=sub||'';
    }
    function updateStats(){
      if(results.length===0){statsWrap.style.display='none';return;}
      statsWrap.style.display='';
      var last=results[results.length-1];
      var best=Math.min.apply(null,results);
      var avg=Math.round(results.reduce(function(a,b){return a+b;},0)/results.length);
      statLast.textContent=last;
      statBest.textContent=best;
      statAvg.textContent=avg;
      statCount.textContent=results.length;
      histList.innerHTML='';
      results.slice(-5).reverse().forEach(function(ms,idx){
        var li=document.createElement('li');
        li.textContent=ms+' ms';
        if(idx===0)li.style.borderColor='var(--primary-color,#6366f1)';
        histList.appendChild(li);
      });
    }
    function startWaiting(){
      setState(S_WAITING);
      setContent(txt.pressHint,'');
    }
    function startGetReady(){
      setState(S_READY);
      setContent(txt.waitMsg,txt.waitSub);
      var delay=2000+Math.random()*3000;
      timer=setTimeout(function(){
        if(state!==S_READY)return;
        goTime=Date.now();
        setState(S_GO);
        setContent(txt.goMsg,'');
      },delay);
    }
    function handleClick(){
      if(state===S_WAITING){startGetReady();return;}
      if(state===S_READY){
        clearTimeout(timer);timer=null;
        setState(S_TOO_EARLY);
        setContent(txt.tooEarly,txt.tooEarlySub);
        return;
      }
      if(state===S_GO){
        var elapsed=Date.now()-goTime;
        goTime=null;
        if(results.length>=5)results.shift();
        results.push(elapsed);
        updateStats();
        setState(S_RESULT);
        boxText.innerHTML='<span class="rt-result-time">'+elapsed+' ms</span>';
        boxSub.textContent=txt.resultSub;
        return;
      }
      if(state===S_TOO_EARLY||state===S_RESULT){startWaiting();return;}
    }
    box.addEventListener('click',handleClick);
    box.addEventListener('keydown',function(e){
      if(e.key==='Enter'||e.key===' '){e.preventDefault();handleClick();}
    });
    startWaiting();
  }());
