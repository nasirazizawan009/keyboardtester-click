<section class="guidelines landing-guide" id="guidelines">
  <div class="help-header">
    <h2>Ghost Click Detector Guide</h2>
    <p>Use this test when a mouse opens files twice, fires extra shots, submits forms twice, or feels like it double-clicks from one press.</p>
  </div>

  <div class="help-grid">
    <div class="help-card">
      <h3>Run a clean test</h3>
      <ol>
        <li>Select the button you want to test.</li>
        <li>Click one deliberate press at a time.</li>
        <li>Aim for at least 30 clicks before judging the result.</li>
      </ol>
    </div>
    <div class="help-card">
      <h3>Use advanced options</h3>
      <ul>
        <li>Start with the recommended 150 ms threshold.</li>
        <li>Use 80 ms for strict hardware bounce evidence.</li>
        <li>Export the report if you need proof for warranty or support.</li>
      </ul>
    </div>
    <div class="help-card">
      <h3>Read the result</h3>
      <ul>
        <li>Zero suspicious intervals usually means the run is clean.</li>
        <li>One suspicious interval should be retested.</li>
        <li>Repeated suspicious intervals on the same button can mean switch bounce.</li>
      </ul>
    </div>
  </div>

  <div class="help-footer">
    <p>The detector is timing evidence, not a physical camera measurement. For stronger confirmation, repeat the same button in another browser or computer before replacing the mouse.</p>
    <p>Need broader checks? Use the mouse tester for every button and scroll wheel, or the click speed test when you want deliberate CPS measurement.</p>
  </div>
</section>
