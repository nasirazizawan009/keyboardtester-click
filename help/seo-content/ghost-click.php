          <p>Cleaning helps in some cases. Use compressed air to blow debris from around buttons. For internal cleaning, you'd need to open the mouse and clean the switch contacts - this is advanced and may void warranty. Cleaning is more likely to help if the mouse is relatively new and the problem started suddenly.</p>
