          <summary>Why does my mouse double-click when I single-click?</summary>
          <p>The most common cause is worn micro-switches inside your mouse. These switches contain metal contacts that degrade over millions of clicks. When worn, they "bounce" - vibrating between on and off states - which your computer reads as multiple clicks. Other causes include static electricity, driver bugs, and USB port issues.</p>
