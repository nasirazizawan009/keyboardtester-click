      <h2>Understanding Mouse DPI and Sensitivity</h2>
      <p>DPI and sensitivity are often confused, but they work differently.</p>

      <h3>What DPI Actually Means</h3>
      <p>DPI is a hardware measurement built into your mouse sensor. At 800 DPI, moving your mouse exactly one inch makes the cursor move 800 pixels on screen. At 1600 DPI, the same movement covers 1600 pixels.</p>
      <p>Higher DPI means:</p>
      <ul>
        <li>Less physical mouse movement needed</li>
        <li>Faster cursor across the screen</li>
        <li>Potentially less precision for small movements</li>
        <li>Better for high-resolution displays</li>
      </ul>

      <h3>DPI vs. Software Sensitivity</h3>
      <p>Your operating system and games apply a sensitivity multiplier on top of DPI:</p>
      <ul>
        <li><strong>Windows pointer speed</strong> - Multiplies or divides raw DPI movement (default is 1:1 at notch 6/11)</li>
        <li><strong>In-game sensitivity</strong> - Further multiplies the result for that specific game</li>
        <li><strong>Mouse software sensitivity</strong> - Some mouse drivers add another layer</li>
      </ul>
      <p>Your effective DPI (eDPI) = Mouse DPI x Software Sensitivity. A player at 800 DPI with 1.5 in-game sens has 1200 eDPI.</p>

      <h3>Why DPI Accuracy Matters</h3>
      <p>If your mouse reports 800 DPI but actually performs at 750 DPI:</p>
      <ul>
        <li>Your muscle memory is slightly off from what you calculated</li>
        <li>Copying pro player settings won't match their actual sensitivity</li>
        <li>Cross-game sensitivity conversions will be inaccurate</li>
        <li>Aim training progress may not transfer correctly</li>
      </ul>
    </article>

