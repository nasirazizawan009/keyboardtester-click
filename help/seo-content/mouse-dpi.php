      <h2>Troubleshooting Mouse DPI Issues</h2>
      <p>Common DPI-related problems and solutions.</p>

      <h3>Cursor Feels Different After Windows Update</h3>
      <ul>
        <li>Check if "Enhance pointer precision" got re-enabled</li>
        <li>Verify pointer speed slider hasn't moved from your preference</li>
        <li>Reinstall mouse drivers - updates sometimes reset profiles</li>
      </ul>

      <h3>DPI Feels Inconsistent</h3>
      <ul>
        <li><strong>Surface issues</strong> - Some sensors struggle with certain mousepad colors or textures</li>
        <li><strong>Dirty sensor</strong> - Clean the lens with compressed air</li>
        <li><strong>Sensor malfunction</strong> - Test on multiple surfaces; if inconsistent everywhere, sensor may be failing. Run a full <a href="mouse-test.php">mouse test</a> to check all functions.</li>
        <li><strong>USB power issues</strong> - Try a different USB port, especially for high-DPI gaming mice</li>
      </ul>

      <h3>DPI Button Not Working</h3>
      <ul>
        <li>Check mouse software for DPI stages - some disable certain levels</li>
        <li>Reset mouse to factory defaults in software</li>
        <li>Button may need cleaning or switch replacement</li>
      </ul>

      <h3>Can't Reach Expected DPI</h3>
      <p>Some mice advertise high DPI (16000+) but interpolate above their native sensor resolution. Real performance may cap at 3200-6400 DPI depending on the sensor. This isn't a defect - interpolated DPI adds input lag and reduces accuracy. Using native DPI is better.</p>
