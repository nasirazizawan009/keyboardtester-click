      <h2>Mouse DPI for Office and Productivity</h2>
      <p>Work tasks have different DPI needs than gaming.</p>

      <h3>Recommended Office DPI Settings</h3>
      <ul>
        <li><strong>Single 1080p monitor</strong> - 800-1200 DPI is comfortable</li>
        <li><strong>Single 1440p/4K monitor</strong> - 1200-1600 DPI prevents excessive mousing</li>
        <li><strong>Dual monitors</strong> - 1400-2000 DPI for efficient screen crossing</li>
        <li><strong>Triple+ monitors or ultrawide</strong> - 1800-2400 DPI or higher</li>
      </ul>

      <h3>Task-Specific Considerations</h3>
      <ul>
        <li><strong>Graphic design / Photo editing</strong> - Lower DPI (600-1000) for precise brush control. Use a tablet for detailed work.</li>
        <li><strong>Video editing / Timeline work</strong> - Medium DPI (1000-1400) balances precision with timeline scrubbing speed.</li>
        <li><strong>Spreadsheet / Data entry</strong> - Higher DPI (1200-1800) for quick cell navigation.</li>
        <li><strong>CAD / 3D modeling</strong> - Depends on viewport navigation. Many prefer lower DPI with scroll-zoom.</li>
      </ul>

      <h3>Ergonomic Benefits of Proper DPI</h3>
      <p>Incorrect DPI causes physical strain:</p>
      <ul>
        <li><strong>Too low</strong> - Excessive arm movement causes shoulder fatigue</li>
        <li><strong>Too high</strong> - Constant micro-corrections cause wrist strain</li>
        <li><strong>Just right</strong> - Natural wrist movement covers your working area</li>
      </ul>
      <p>Test different DPIs for a week each. The right setting feels effortless - you reach screen corners without overshooting or needing multiple swipes.</p>
