      <h2>Best Way to Run a Stereo Sound Test</h2>
      <ul>
        <li>Start with isolated left and right channel playback.</li>
        <li>Run stereo playback to make sure both sides work together.</li>
        <li>Use bass, treble, sweep, or noise modes if one side sounds weak only in certain ranges.</li>
        <li>Retest after changing the output device or audio settings.</li>
      </ul>
