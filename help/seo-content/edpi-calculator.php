      <h2>What Is eDPI?</h2>
      <p>eDPI (effective DPI) is the standard metric for comparing mouse aim across different DPI/sensitivity settings and different games. The formula is simple: <strong>eDPI = mouse DPI x in-game sensitivity</strong>. A player at 800 DPI / 0.5 sens has the same effective aim as one at 400 DPI / 1.0 sens - both equal 400 eDPI.</p>
      <p>Pro CS2 / Valorant players cluster between 200-400 eDPI. Pro Apex players cluster 800-1600 eDPI. Knowing your eDPI lets you copy a pro setting reliably or compare yourself meaningfully to benchmarks.</p>
    </article>
