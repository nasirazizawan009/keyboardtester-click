      <h2>Pro eDPI Reference Points</h2>
      <ul>
        <li><strong>CS2 / CS:GO:</strong> 200-400 eDPI typical (s1mple 320, ZywOo 360, NiKo 400)</li>
        <li><strong>Valorant:</strong> 240-320 eDPI typical (TenZ 280, ScreaM 240)</li>
        <li><strong>Apex Legends:</strong> 800-1600 eDPI (faster game, faster look speed needed)</li>
        <li><strong>Overwatch:</strong> 3000-4500 eDPI (track-heavy heroes prefer faster)</li>
        <li><strong>Fortnite:</strong> 60-150 eDPI (very low for build precision)</li>
      </ul>
    </article>
    <article class="seo-article">
      <h2>cm/360 - The Other Reference</h2>
      <p>cm/360 (centimeters per 360-degree turn) is the physical distance you need to mouse-swipe to do a complete spin in-game. Lower cm/360 = faster sensitivity. Most CS pros play 30-50 cm/360. Apex pros 18-25 cm/360. Calculated from eDPI and the games yaw multiplier (Source engine = 0.022, Valorant = 0.07).</p>
