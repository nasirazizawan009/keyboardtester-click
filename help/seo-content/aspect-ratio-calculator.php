      <h2>What Is Aspect Ratio?</h2>
      <p>Aspect ratio is the relationship between a rectangle's width and height, expressed as two whole numbers (like 16:9) or a decimal (like 1.7778). It determines whether content feels wide and cinematic, tall like a phone screen, or classic like a 4:3 TV. Two resolutions with the same aspect ratio — say 1920×1080 and 3840×2160 — will display the same image content just at different pixel densities. This calculator handles the three directions: given any two of (width, height, ratio), it solves for the third and auto-simplifies to lowest integer terms.</p>
    </article>
    <article class="seo-article">
      <h2>The Math</h2>
      <p>Aspect ratio is literally <strong>width / height</strong>. To simplify 1920×1080 to 16:9, divide both by their greatest common divisor (120): 1920/120 = 16, 1080/120 = 9. To find the width given ratio 16:9 and height 720, multiply: 720 × (16/9) = 1280. The calculator runs both flows live — type whichever two fields you know and it locks them in, solving for the third.</p>
