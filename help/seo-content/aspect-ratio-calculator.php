      <h2>Common Aspect Ratios By Use Case</h2>
      <ul>
        <li><strong>16:9</strong> — modern HDTVs, all 1080p / 1440p / 4K gaming monitors, most YouTube.</li>
        <li><strong>16:10</strong> — MacBooks and productivity-focused laptops (extra vertical space).</li>
        <li><strong>21:9</strong> — ultrawide monitors: 2560×1080, 3440×1440, 5120×2160.</li>
        <li><strong>32:9</strong> — super-ultrawide: 3840×1080, 5120×1440 (essentially dual 16:9).</li>
        <li><strong>4:3</strong> — classic CRT TVs, retro gaming, older productivity monitors.</li>
        <li><strong>1:1</strong> — Instagram square posts, some art prints.</li>
        <li><strong>9:16</strong> — mobile portrait: TikTok, Reels, Shorts.</li>
      </ul>
