      <h2>Resizing Without Distortion</h2>
      <p>When you resize an image or video frame, you preserve visual fidelity by keeping the aspect ratio constant. Need a 4K (3840×2160) asset scaled to fit a 720-pixel-tall column? Use the calculator: 720 × (3840/2160) = 1280 wide. Changing just one dimension (stretching) produces letterboxing or distortion. Most image editors and video tools let you "lock ratio" — the calculator gives you the target numbers to type in.</p>
