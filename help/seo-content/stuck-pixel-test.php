      <h2>Stuck Pixel Test FAQ</h2>
      <div class="faq-list">
        <details class="faq-item">
          <summary>What color is a stuck pixel?</summary>
          <p>Stuck pixels often appear red, green, blue, or white because one or more sub-pixels remain active when they should change with the rest of the screen.</p>
        </details>
        <details class="faq-item">
          <summary>Should I test in full-screen mode?</summary>
          <p>Yes. Full-screen mode removes browser bars and distractions so you can inspect the whole panel, including edges and corners.</p>
        </details>
        <details class="faq-item">
          <summary>Is a stuck pixel the same as backlight bleed?</summary>
          <p>No. Backlight bleed is a larger glow or uneven brightness pattern, while a stuck pixel is a tiny single-pixel or sub-pixel defect.</p>
        </details>
