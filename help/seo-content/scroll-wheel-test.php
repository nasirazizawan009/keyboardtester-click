      <h2>How to Diagnose Scroll-Wheel Problems</h2>
      <ul>
        <li>Roll upward several times and confirm the scroll counter rises steadily.</li>
        <li>Roll downward and make sure the status flips direction correctly.</li>
        <li>Press the wheel once or twice to confirm middle click still registers.</li>
        <li>If the direction is reversed only in one app, compare browser behavior with your operating-system scroll settings before blaming the hardware.</li>
      </ul>
    </article>

