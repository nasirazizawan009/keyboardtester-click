      <h2>What a Scroll Wheel Test Checks</h2>
      <p>A scroll wheel test helps you confirm whether your browser is receiving clean up/down wheel events and whether the middle button still works when you press the wheel. It is useful when scrolling feels jumpy, reversed, delayed, or inconsistent.</p>
