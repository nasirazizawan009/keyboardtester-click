      <h2>How to Get a Fast Yes or No Answer</h2>
      <ol>
        <li>Click start and allow microphone access.</li>
        <li>Speak normally into the microphone.</li>
        <li>Watch the meter and peak value.</li>
        <li>If the meter moves, the browser is receiving your voice input.</li>
      </ol>
      <p>If nothing moves, the most common causes are blocked browser permission, muted hardware, or the wrong input device being active in the operating system.</p>
