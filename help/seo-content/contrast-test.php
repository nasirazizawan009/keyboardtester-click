      <h2>What Is a Contrast Test?</h2>
      <p>A monitor contrast test displays a fine black-and-white checkerboard next to a solid middle gray (RGB 128). When you squint or step back, the checkerboard's perceived average should match the gray. If it doesn't, your monitor's contrast or gamma is misconfigured - typically gamma is too high (perceived darker than middle gray) or too low (perceived brighter).</p>
    </article>
    <article class="seo-article">
      <h2>How to Read the Result</h2>
      <ul>
        <li><strong>Checkerboard matches gray when squinted:</strong> Excellent contrast and gamma calibration.</li>
        <li><strong>Checkerboard appears darker:</strong> Gamma too high (2.4+) or contrast too high. Reduce contrast in OSD.</li>
        <li><strong>Checkerboard appears brighter:</strong> Gamma too low (under 2.0) or brightness washed out. Run our <a href="<?php echo url('monitor-gamma-test.php'); ?>">gamma test</a>.</li>
