      <h2>Keyboard Tester FAQ</h2>
      <div class="faq-list">
        <details>
          <summary>Why doesn't my keyboard show all the keys I'm pressing?</summary>
          <p>This is keyboard ghosting - a hardware limitation where certain key combinations conflict. Your keyboard's matrix can't distinguish which keys are pressed in that combination. This isn't fixable; you need a keyboard with better anti-ghosting or N-key rollover.</p>
        </details>
        <details>
          <summary>Can I test mechanical keyboard switches?</summary>
          <p>Yes. Our tester detects any key press regardless of switch type. It helps identify which specific switch is failing on mechanical keyboards, making targeted repairs possible rather than replacing the whole keyboard.</p>
        </details>
        <details>
          <summary>Why does a key keep repeating or stay highlighted?</summary>
          <p>The key is stuck physically or electrically. Try removing the keycap and cleaning underneath with compressed air. On mechanical keyboards, the switch may be failing. On membrane keyboards, debris or liquid damage may be causing constant contact.</p>
        </details>
        <details>
          <summary>How do I test laptop function keys?</summary>
          <p>Most laptops require holding Fn to send F1-F12 codes (otherwise they send media/brightness). The Fn key itself usually doesn't register in browsers - it's processed by hardware. Test that Fn + F-key combinations perform their expected functions instead.</p>
        </details>
        <details>
          <summary>Is this test accurate for gaming?</summary>
          <p>Yes. Our tester receives the same key events that games receive. If a key registers here, it works in games. If it doesn't register here, it won't work in games either. Use this to verify your setup before competitive play.</p>
        </details>
        <details>
          <summary>Why don't some special keys register?</summary>
          <p>Some keys (like media controls, brightness, Fn) are handled by keyboard firmware before reaching the operating system. They work in their intended function but don't send standard key codes that browsers can detect. Test these by verifying their actual function works.</p>
        </details>
        <details>
          <summary>Can I test keyboard latency with this tool?</summary>
          <p>Our tester shows whether keys register, which helps identify latency-related issues like missed rapid inputs. For precise latency measurements, you'd need specialized hardware. But if keys consistently register during fast typing, latency is likely fine.</p>
        </details>
        <details>
          <summary>How do I fix a key that works sometimes?</summary>
          <p>Intermittent keys usually have debris, a loose connection, or a failing switch. Try: 1) Remove and clean the keycap, 2) Use compressed air around the switch, 3) Check the USB connection stability, 4) If mechanical, the switch may need replacement.</p>
        </details>
        <details>
          <summary>Does this work with non-English keyboard layouts?</summary>
          <p>Yes. We detect key press events regardless of layout. The physical key locations highlight correctly, though the displayed characters match standard layouts. AZERTY, Dvorak, Colemak, and international layouts all work.</p>
        </details>
        <details>
          <summary>Why is my wireless keyboard missing key presses?</summary>
          <p>Common causes: low batteries, interference from other 2.4GHz devices, receiver too far away, or USB power saving disconnecting the receiver. Try fresh batteries, move the receiver closer, and disable USB power management in Windows.</p>
        </details>
