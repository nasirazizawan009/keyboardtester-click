      <h2>Troubleshooting Common Keyboard Problems</h2>
      <p>Here are fixes for issues our keyboard tester helps identify.</p>

      <h3>Keys Not Registering</h3>
      <ul>
        <li><strong>Single key dead</strong> - Debris under keycap, or failed switch. Remove keycap, clean, and test.</li>
        <li><strong>Multiple keys dead</strong> - Check USB connection, try different port, reinstall drivers.</li>
        <li><strong>Wireless not responding</strong> - Replace batteries, move receiver closer, re-pair.</li>
      </ul>

      <h3>Keys Sticking or Repeating</h3>
      <ul>
        <li><strong>Physical debris</strong> - Remove keycap, clean with compressed air, replace cap.</li>
        <li><strong>Spill damage</strong> - May need professional cleaning or switch replacement.</li>
        <li><strong>Software issue</strong> - Check Windows repeat rate settings, update drivers.</li>
      </ul>

      <h3>Ghosting Problems</h3>
      <ul>
        <li><strong>It's not fixable on that keyboard</strong> - Ghosting is a hardware limitation.</li>
        <li><strong>Solution</strong> - Upgrade to a keyboard with better anti-ghosting or NKRO.</li>
        <li><strong>Workaround</strong> - Rebind game keys to combinations that work on your keyboard.</li>
      </ul>

      <h3>Intermittent Connection Issues</h3>
      <ul>
        <li>Check USB cable for damage (wiggle while testing)</li>
        <li>Try different USB ports (prefer back panel on desktops)</li>
        <li>Disable USB power saving in Windows Device Manager</li>
        <li>Replace cable if it's detachable</li>
      </ul>
