      <h2>Understanding Keyboard Ghosting</h2>
      <p>Keyboard ghosting is a hardware limitation that affects multi-key presses.</p>

      <h3>What Is Ghosting?</h3>
      <p>Ghosting occurs when pressing multiple keys simultaneously causes:</p>
      <ul>
        <li>Some pressed keys not registering at all</li>
        <li>Phantom keys appearing that weren't pressed</li>
        <li>Keys being blocked to prevent phantom inputs</li>
      </ul>
      <p>This happens because most keyboards use a key matrix where rows and columns of wires intersect. Certain key combinations create electrical ambiguity about which keys are actually pressed.</p>

      <h3>Key Rollover Explained</h3>
      <ul>
        <li><strong>2-Key Rollover (2KRO)</strong> - Guarantees 2 simultaneous keys, common on very old keyboards</li>
        <li><strong>6-Key Rollover (6KRO)</strong> - Standard for USB keyboards, handles most normal typing</li>
        <li><strong>N-Key Rollover (NKRO)</strong> - Every key works independently, no combinations blocked</li>
        <li><strong>Anti-ghosting</strong> - Certain key groups (like WASD + Shift + Space) work even without full NKRO</li>
      </ul>

      <h3>Testing for Ghosting</h3>
      <p>Our tester shows ghosting by highlighting all detected keys. If you press three keys but only two highlight, you've found a ghosting limitation. Try these test combinations:</p>
      <ul>
        <li>WASD (gaming movement) - should all register together</li>
        <li>QWE + ASD (simultaneous rows) - often reveals ghosting</li>
        <li>Multiple keys plus Shift - common gaming scenario</li>
        <li>Ctrl + Shift + letter combinations - productivity shortcuts</li>
      </ul>
