      <h2>How to Read the dB Numbers</h2>
      <ul>
        <li><strong>30-40 dB:</strong> Whisper, very quiet room.</li>
        <li><strong>50-60 dB:</strong> Normal conversation.</li>
        <li><strong>70-80 dB:</strong> Vacuum cleaner, busy traffic.</li>
        <li><strong>85+ dB:</strong> Hearing damage risk with prolonged exposure.</li>
        <li><strong>100-110 dB:</strong> Concert, motorcycle.</li>
        <li><strong>120+ dB:</strong> Pain threshold, immediate damage risk.</li>
      </ul>
