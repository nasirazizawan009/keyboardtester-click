      <h2>Optimization Tips</h2>
      <ul>
        <li><strong>Disable mouse acceleration first.</strong> Windows "Enhance pointer precision" adds a non-linear scale that poisons every measurement and makes consistent aim impossible. Turn it off in <em>Control Panel → Mouse → Pointer Options</em>. Set pointer speed to 6/11 (the middle notch) so Windows applies no scaling.</li>
        <li><strong>Pick a DPI that matches your screen.</strong> On 1080p, 400-800 DPI is plenty for FPS aim. On 1440p, 800-1600 feels more natural. On 4K, 1600+ stops feeling "sluggish" in productivity apps.</li>
        <li><strong>Stay under 3200 DPI for FPS.</strong> Higher DPI means tinier physical movements for the same crosshair travel — small sensor jitter becomes visible at very high DPI. Most pros sit between 400 and 1600.</li>
        <li><strong>Set polling rate to 1000 Hz (or 4000+ if supported).</strong> This controls how often the mouse reports position, not how far it moves per inch. Separate from DPI.</li>
        <li><strong>Measure on the same surface you play on.</strong> Cloth vs. hard pads can shift reported DPI by a few percent. Use the measure tab while the mouse is on your actual mouse pad.</li>
      </ul>
    </article>

    <article class="seo-article">
      <h2>Common Mistakes to Avoid</h2>
      <ul>
        <li><strong>Changing DPI without converting sensitivity.</strong> The single biggest aim-wrecker. You double your DPI, your eDPI doubles, every flick overshoots. Use the converter.</li>
        <li><strong>Trusting advertised DPI without verification.</strong> Mouse software DPI settings and actual sensor output can disagree. Always measure first if you're copying a pro's exact setup.</li>
        <li><strong>Using a tiny swipe to measure.</strong> A 1-inch swipe amplifies rounding errors. Aim for 4-8 inches of real movement on the measure tab for an accurate reading.</li>
        <li><strong>Ignoring the game's yaw value when switching titles.</strong> Valorant at sens 0.4 is not Valorant at sens 0.4 in CS2. Yaw differs per game — the converter handles this when you pick a game.</li>
        <li><strong>Chasing pro eDPI blindly.</strong> Pros use low eDPI because they have huge mousepads and room. If you play at a desk with a small pad, 280 eDPI in Valorant may be too low to turn around. Stay in the range, don't copy exact numbers.</li>
      </ul>
