      <h2>How to Test Your Microphone</h2>
      <p>Follow this process for a complete microphone test.</p>

      <h3>Step 1: Grant Browser Permission</h3>
      <ol>
        <li>Click the "Start Test" button on our tool</li>
        <li>When prompted, click "Allow" to grant microphone access</li>
        <li>If you accidentally denied, click the camera/mic icon in your browser's address bar to change permission</li>
      </ol>

      <h3>Step 2: Select Correct Input</h3>
      <ol>
        <li>If you have multiple microphones, select the right one from the dropdown</li>
        <li>Your device names appear as Windows/Mac shows them</li>
        <li>If your mic doesn't appear, check physical connections</li>
      </ol>

      <h3>Step 3: Check Audio Levels</h3>
      <ol>
        <li>Speak at your normal volume</li>
        <li>Watch the level meter respond</li>
        <li><strong>Green</strong> - Good level, normal speech range</li>
        <li><strong>Yellow</strong> - Getting loud, may clip at peaks</li>
        <li><strong>Red</strong> - Too loud, audio is clipping/distorting</li>
      </ol>

      <h3>Step 4: Listen to Playback</h3>
      <ol>
        <li>Record a short sample of your voice</li>
        <li>Play it back through headphones (not speakers, to avoid feedback)</li>
        <li>Listen for: clarity, background noise, echo, distortion</li>
      </ol>

      <h3>Step 5: Test Different Scenarios</h3>
      <ul>
        <li>Speak at different volumes (whisper to loud)</li>
        <li>Test keyboard sounds (will teammates hear your typing?)</li>
        <li>Make P and B sounds (check for plosives)</li>
        <li>Be silent (check background noise level)</li>
      </ul>
