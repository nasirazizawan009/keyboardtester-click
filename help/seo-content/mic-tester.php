      <h2>Microphone Test FAQ</h2>
      <div class="faq-list">
        <details>
          <summary>Why doesn't my browser detect my microphone?</summary>
          <p>Check that you granted permission when prompted (click "Allow"). If you denied, go to browser settings > Privacy/Security > Site Settings > Microphone and allow this site. Also verify the mic is physically connected and enabled in Windows/Mac sound settings.</p>
        </details>
        <details>
          <summary>Can I test my headset microphone?</summary>
          <p>Yes. Our tester works with any microphone your computer recognizes - headset mics, USB microphones, laptop built-in mics, wireless headsets, and XLR mics through audio interfaces. Select your device from the dropdown if multiple are available.</p>
        </details>
        <details>
          <summary>Why is my mic picking up too much background noise?</summary>
          <p>Condenser microphones are sensitive and pick up ambient sounds. Move closer to the mic (improves voice-to-noise ratio), close noise sources, enable noise suppression in your apps, or consider a dynamic microphone which rejects more background noise.</p>
        </details>
        <details>
          <summary>How do I reduce echo in my audio?</summary>
          <p>Echo occurs when your mic picks up audio from your speakers. Use headphones instead of speakers to eliminate this. If you must use speakers, lower their volume, move them away from the mic, or enable echo cancellation in your communication app.</p>
        </details>
        <details>
          <summary>Is this microphone test private?</summary>
          <p>Yes. All audio processing happens locally in your browser. We do not record, store, or transmit any audio. Your voice never leaves your computer. We only use the microphone to display visual levels and allow you to hear playback locally.</p>
        </details>
        <details>
          <summary>Why does my mic work here but not in Zoom/Discord?</summary>
          <p>Each application has its own microphone selection. In your app's audio settings, make sure the correct microphone is selected - it may be defaulting to a different device. Also check that the app has microphone permissions in your system settings.</p>
        </details>
        <details>
          <summary>What's the best mic position?</summary>
          <p>Position your mic 6-12 inches from your mouth, slightly off to the side (not directly in front). This reduces plosives (popping P/B sounds) while maintaining good pickup. Headset mics should be at the corner of your mouth.</p>
        </details>
        <details>
          <summary>Why is my voice too quiet in the test?</summary>
          <p>Increase the input gain in Windows Sound Settings (Recording devices > Properties > Levels). Move closer to the microphone. Check that you're speaking into the correct side of the mic (many have front-address, not top-address). Disable any noise gates that may be cutting quiet sounds.</p>
        </details>
        <details>
          <summary>How do I stop my mic from clipping?</summary>
          <p>Clipping happens when input is too loud. Lower the gain/input level in your system sound settings. Move back from the microphone. Speak at normal conversational volume instead of projecting. The level meter should peak in yellow, not red.</p>
        </details>
        <details>
          <summary>Should I use my laptop mic or get an external one?</summary>
          <p>Laptop mics are convenient but usually lower quality. For important calls or any content creation, a USB microphone ($30-100) significantly improves audio. For casual calls, laptop mics work fine. Headset mics are a good middle ground.</p>
