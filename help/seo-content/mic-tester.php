      <h2>Common Microphone Problems and Fixes</h2>
      <p>Here are the issues our microphone tester helps diagnose.</p>

      <h3>Microphone Not Detected</h3>
      <p>The mic doesn't appear in the device list.</p>
      <ul>
        <li><strong>Check physical connection</strong> - USB fully plugged in, 3.5mm in correct port</li>
        <li><strong>Try different port</strong> - USB ports can fail; try another one</li>
        <li><strong>Check Windows Sound settings</strong> - Mic may be disabled</li>
        <li><strong>Install drivers</strong> - Some USB mics need manufacturer drivers</li>
        <li><strong>Restart browser</strong> - Browser may not have detected the device</li>
      </ul>

      <h3>No Audio Input / Silent</h3>
      <p>Mic is detected but shows no levels.</p>
      <ul>
        <li><strong>Check mute switch</strong> - Many mics have physical mute buttons</li>
        <li><strong>Check Windows levels</strong> - Input volume may be at zero</li>
        <li><strong>Check app exclusivity</strong> - Another app may have exclusive mic access</li>
        <li><strong>Browser permission</strong> - Site may be blocked from mic access</li>
        <li><strong>Wrong mic selected</strong> - You may be testing a different device</li>
      </ul>

      <h3>Very Low Volume</h3>
      <p>Levels barely register, voice sounds distant.</p>
      <ul>
        <li><strong>Increase gain</strong> - In Windows Sound > Recording > Properties > Levels</li>
        <li><strong>Move closer</strong> - Position mic 6-12 inches from mouth</li>
        <li><strong>Check mic pattern</strong> - Speak into the correct side (usually front)</li>
        <li><strong>Disable noise gate</strong> - Some software cuts quiet sounds</li>
      </ul>

      <h3>Distortion and Clipping</h3>
      <p>Audio sounds harsh, crackly, or broken.</p>
      <ul>
        <li><strong>Lower gain</strong> - Reduce input level until peaks stay in green/yellow</li>
        <li><strong>Move back</strong> - You're too close, overwhelming the mic</li>
        <li><strong>Lower voice</strong> - Speak at conversational volume, not shouting</li>
        <li><strong>Check hardware</strong> - Damaged cables or failing mic can cause distortion</li>
      </ul>

      <h3>Background Noise</h3>
      <p>Sounds other than your voice are audible.</p>
      <ul>
        <li><strong>Close noise sources</strong> - Fans, AC, open windows</li>
        <li><strong>Get closer to mic</strong> - Better voice-to-noise ratio</li>
        <li><strong>Use noise suppression</strong> - Built into Discord, Zoom, Windows</li>
        <li><strong>Consider dynamic mic</strong> - Less sensitive to room noise than condenser mics</li>
      </ul>

      <h3>Echo and Feedback</h3>
      <p>You hear yourself delayed, or audio feeds back.</p>
      <ul>
        <li><strong>Use headphones</strong> - Speakers playing into mic causes echo</li>
        <li><strong>Lower speaker volume</strong> - If you must use speakers</li>
        <li><strong>Enable echo cancellation</strong> - In your communication app settings</li>
        <li><strong>Move speakers away</strong> - More distance reduces feedback risk</li>
      </ul>
    </article>

