      <h2>What This Microphone Tester Can Verify</h2>
      <ul>
        <li>Whether the browser can access your microphone</li>
        <li>Whether live input levels respond when you speak</li>
        <li>How loud your input gets at peak volume</li>
        <li>Whether the mic appears completely silent in the browser</li>
      </ul>
      <p>The current tool is designed for live input confirmation, not recording or playback.</p>
