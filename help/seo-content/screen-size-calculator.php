      <h2>Fitting A TV To A Space</h2>
      <p>Before ordering a new TV, measure the wall or cabinet width you have. Use this calculator to confirm the advertised diagonal fits. A "75-inch TV" is 65.4 inches wide (for 16:9) — it might not fit a 60-inch-wide media stand. Also factor in the TV bezel (usually +0.5 to +2 inches on each side) and any stand or wall-mount hardware.</p>
