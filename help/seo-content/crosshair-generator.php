      <p>A crosshair generator lets you design the aiming reticle for your FPS of choice without having to alt-tab into the game and guess. You tweak shape, color, thickness, length, gap, outline and dot, then either download the result as a transparent PNG overlay or copy a game-specific config string you paste into the in-game console. This tool does all three.</p>
