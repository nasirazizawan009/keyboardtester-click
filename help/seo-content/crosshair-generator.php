      <h2>CS2 / CS:GO Console Command</h2>
      <p>The copyable CS2 string uses the full <code>cl_crosshair*</code> family: style, size, thickness, gap, color (RGB), outline, outline thickness, alpha and T-shape flag. Open the developer console with the <strong>tilde key (~)</strong> in-game and paste the whole line. You can add the command to your <code>autoexec.cfg</code> so your crosshair loads every match. Popular pro settings are included as presets (s1mple, cs_pro_classic).</p>
