      <h2>How to Read the Results</h2>
      <p>If every key you are holding lights up, the combo is supported. If one key disappears or never registers, you have found a blocked combination. If the problem only appears in some areas of the keyboard, that usually points to the matrix layout rather than browser behavior.</p>
      <p>For a broader check of all keys and layouts, go back to the main <a href="index.php">keyboard tester</a>.</p>
