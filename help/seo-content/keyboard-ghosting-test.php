      <h2>How to Use This Keyboard Ghosting Test</h2>
      <p>Press three or more keys simultaneously. If all keys light up on the virtual keyboard, your keyboard handles that combination correctly. If one key fails to light up, that combination is ghosted. Test gaming combinations like W+A+Shift+Space to check the keys most critical for gaming performance.</p>
