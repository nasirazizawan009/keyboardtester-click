      <div class="faq-list">
        <details class="faq-item">
          <summary>Is keyboard ghosting bad for gaming?</summary>
          <p>Yes, especially if it affects your common movement or ability keys. Missed inputs can make a keyboard feel inconsistent even when single keys work normally.</p>
        </details>
        <details class="faq-item">
          <summary>Do all keyboards have ghosting?</summary>
          <p>Most keyboards have some limit, but better boards are designed so the most common combinations still register correctly.</p>
        </details>
        <details class="faq-item">
          <summary>Can a browser test really show ghosting?</summary>
          <p>Yes. The browser can only display keys that the operating system receives, so missing highlights during a real multi-key press are still useful evidence of a hardware limit.</p>
